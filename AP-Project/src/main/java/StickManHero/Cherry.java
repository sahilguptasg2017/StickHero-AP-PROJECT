package StickManHero;

public class Cherry {

    private int count ;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void incCount(int count){

        this.count += count ;
    }


    public void decCount(int count){

        this.count -= count ;
    }




}
