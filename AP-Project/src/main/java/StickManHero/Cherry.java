package StickManHero;

public class Cherry {

    private int count ;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void incrCount(int count){

        this.count += count ;
    }


    public void decrCount(int count){

        this.count -= count ;
    }




}
